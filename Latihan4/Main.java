package Latihan4;

/* Variabel, Assignment dan Deklarasi */

public class Main {
    public static void main(String[] args) {
        /* Variabel */
        int a=10;
        System.out.println("nilai a = " + a);
        /* Kita juga dapat mengubah nilai variabel yang sudah di print dengan cara */
        a=20;
        System.out.println("nilai a baru = " + a);

        /* Deklarasi */
        int b;
        b=10;
        System.out.println("nilai b = " + b);
        /* Kita juga dapat mengubah nilai deklarasi yang sudah di print dengan cara */
        b=15;
        System.out.println("nilai b baru = " + b);
    }
}
